# Example for dictionary

# Creating an empty dictionary
my_dict = dict()

my_dict["name"] = "SiriSarah LLC"
my_dict["age"] = 1

print(my_dict)

print(my_dict["name"])
print(my_dict["age"])

for name, age in my_dict.keys(), my_dict.values():
    print(name, age)

