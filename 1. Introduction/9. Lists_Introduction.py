# Lists 

marks = [50, 45, 30, 20, 0]

# Print all the values in the list
print(marks)

# Print the fist element in the list
print(marks[0])

# Print the last element in the list
print(marks[-1])

# Print the length of the list
print(len(marks))

# Print the first four values
print(marks[0:4])

# Print the last four values
print(marks[1:5])

