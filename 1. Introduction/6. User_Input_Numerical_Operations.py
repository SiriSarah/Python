# Get input from users and then perform numerical operation

number_1 = int(input("Enter the first integer: "))
number_2 = int(input("Enter the second integer: "))

# Example of using variables and operations in print statement
print (f"The sum of {number_1} and {number_2} is {number_1 + number_2}")

# Alternative method is to create a new variable and store the output
sum_of_2_numbers = number_1 + number_2
print(f"The sum of {number_1} and {number_2} is {sum_of_2_numbers}")

